/* USER CODE BEGIN 4 */
void AudioDSP_Init(void)
{
    memset((void*)pcm16_rx, 0, sizeof(pcm16_rx));
    memset((void*)scaleMag, 0, sizeof(scaleMag));

    InitScales();
    StartUartDmaReception();
}

static void InitScales(void)
{
    float s0 = 2.0f * DT; // Escala base
    float dj = 0.25f;     // Espaciado entre escalas

    for (int i = 0; i < J_TOT; ++i) {
        cwtScale[i] = s0 * powf(2.0f, (float)(i) * dj);
    }
}

static void StartUartDmaReception(void)
{
	HAL_UART_Receive_DMA(&huart2, (uint8_t*)pcm16_rx, UART_DMA_RX_BYTES);
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == USART2)
    {
        frameReady = true;
    }
}

void AudioDSP_Task(void)
{
    if (frameReady)
    {
        frameReady = false;
        ProcessCWT_F32();
        SendScaleMatrix();

        StartUartDmaReception();
    }
}

static void ProcessCWT_F32(void)
{
    // int16 a float
    for (int i = 0; i < N_SAMPLES; i++) {
        yInput[i] = (float)pcm16_rx[i];
    }

    cwavelet(yInput, N_SAMPLES, DT, 0, 6.0f, cwtScale[0], 0.25f, J_TOT, N_SAMPLES,
             cwtWave, cwtScale, cwtPeriod, cwtCoi);

    // magnitud basada en las escalas
    for (int j = 0; j < J_TOT; j++) {
        int iter = 2 * j * N_SAMPLES;
        for (int i = 0; i < N_SAMPLES; i++) {
            float re = cwtWave[iter + 2 * i];
            float im = cwtWave[iter + 2 * i + 1];
            scaleMag[j][i] = sqrtf(re * re + im * im);
        }
    }
}

static void SendScaleMatrix(void)
{
    const uint8_t syncHeader[4] = {255, 170, 255, 170}; // Header de sincronizacion

    HAL_UART_Transmit(&huart2, (uint8_t*)syncHeader, sizeof(syncHeader), HAL_MAX_DELAY);
    HAL_UART_Transmit(&huart2, (uint8_t*)scaleMag, sizeof(scaleMag), HAL_MAX_DELAY);
}

void HAL_UART_ErrorCallback(UART_HandleTypeDef *huart)
{
    if (huart->Instance == USART2)
    {
        __HAL_UART_CLEAR_OREFLAG(huart);
        StartUartDmaReception();
    }
}
